package edu.uta.futureye.function;

/**
 * Used for construct objects of type <tt>Variable</tt>
 * 
 * @author liuyueming
 *
 */
public class VarPair {
	public String name;
	public double value;

	public VarPair(String name, double value) {
		this.name = name;
		this.value = value;
	}
}
